// 두 수의 합
function solution(num1, num2) {
    return num1 + num2
}

// 두 수의 차
function solution(num1, num2) {
    return num1 - num2
}

// 두 수의 곱
function solution(num1, num2) {
    return num1 * num2
}

// 몫 구하기
function solution(num1, num2) {
    // Math.floor() 소수점 이하 버림
    return Math.floor(num1 / num2);
}